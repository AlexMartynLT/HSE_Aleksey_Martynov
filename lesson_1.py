# Задание 1: Работа с переменными и ввод/вывод
# Создаем переменные разных типов и выводим их
number = 5  # Целое число
text = "Пример текста"  # Строка
decimal = 3.14  # Число с плавающей точкой

print("Переменная number:", number)
print("Текст:", text)
print("Число с плавающей точкой:", decimal)

# Запрашиваем у пользователя данные
user_num = int(input("Введите ваше число: "))  # Преобразуем в целое число
user_str = input("Введите ваш текст: ")  # Просто строка

print("\nВы ввели:")
print("Число:", user_num)
print("Текст:", user_str)

# Проверка изменения адреса объектов через id()
my_list = [1, 2, 3]  # Список (изменяемый объект)
print("\nID списка до изменения:", id(my_list))
my_list.append(4)  # Изменяем список, но адрес остается тем же
print("ID после изменения:", id(my_list))

num = 5  # Целое число (неизменяемый объект)
print("\nID числа 5:", id(num))
num += 1  # Создаем новое число, адрес меняется
print("ID после присвоения 6:", id(num))  # Новый объект в памяти


# Задание 2: Перевод времени в часы, минуты, секунды
time_str = input("\nВведите время в секундах: ")

# Проверяем, что введено число
if not time_str.isdigit():
    print("Ошибка: введите только цифры")
else:
    total_seconds = int(time_str)  # Преобразуем строку в целое число
    
    # Вычисляем часы, минуты и секунды
    hours = total_seconds // 3600  # Часы = целое деление на 3600
    remaining_seconds = total_seconds % 3600  # Остаток после часов
    minutes = remaining_seconds // 60  # Минуты = целое деление остатка на 60
    seconds = remaining_seconds % 60  # Остаток после минут
    
    print("\nРезультат:")
    print(f"Часов: {hours}, Минут: {minutes}, Секунд: {seconds}")


# Задание 3: Сумма n + nn + nnn
n = input("\nВведите число от 1 до 9: ")

# Формируем числа nn и nnn через строковые операции
n_str = str(n)
nn = n_str * 2  # Например, "3" → "33"
nnn = n_str * 3  # Например, "3" → "333"

sum_result = int(n) + int(nn) + int(nnn)
print(f"\n{n} + {nn} + {nnn} = {sum_result}")
