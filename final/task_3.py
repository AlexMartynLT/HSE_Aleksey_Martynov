# === НАЧАЛО ФУНКЦИИ ===
def is_monotonic(nums):
    """
    Эта функция принимает на вход список чисел (например, [1, 2, 2, 3])
    и определяет, является ли он монотонным (то есть только возрастающим
    или только убывающим).
    Возвращает True, если он монотонный, и False в противном случае.
    """

    # --- 1. Подготовительный этап ---

    # Создаем два логических "флага". Вначале мы оптимистично предполагаем,
    # что последовательность может быть и возрастающей, и убывающей.
    is_increasing = True
    is_decreasing = True

    # --- 2. Основной цикл ---

    # Запускаем цикл, который будет перебирать элементы списка попарно.
    # range(len(nums) - 1) - это важно, чтобы остановиться на ПРЕДПОСЛЕДНЕМ элементе
    # и не выйти за границы списка при проверке nums[i+1].
    for i in range(len(nums) - 1):

        # --- Проверка на нарушение правил ---

        # Если текущий элемент (nums[i]) оказался БОЛЬШЕ следующего (nums[i+1]),
        # значит, правило "неубывания" нарушено. Опускаем флаг.
        if nums[i] > nums[i + 1]:
            is_increasing = False

        # Если текущий элемент (nums[i]) оказался МЕНЬШЕ следующего (nums[i+1]),
        # значит, правило "невозрастания" нарушено. Опускаем флаг.
        if nums[i] < nums[i + 1]:
            is_decreasing = False

    # --- 3. Возврат результата ---

    # После того как цикл прошелся по всем парам, смотрим на флаги.
    # Если ХОТЯ БЫ ОДИН из флагов остался True (используем оператор 'or'),
    # значит, последовательность монотонна.
    return is_increasing or is_decreasing


