# === НАЧАЛО ИНТЕРАКТИВНОЙ ЧАСТИ ===

print("Программа для проверки монотонности массива чисел.")
# 1. Запрашиваем у пользователя строку с числами.
#    Предлагаем ему удобный формат: числа через пробел.
user_input_string = input("Введите числа, разделяя их пробелами (например: 1 2 2 3): ")

# 2. Превращаем строку в список чисел. Это происходит в три этапа:
try:
    # 2.1. .split() - этот метод "разрезает" строку по пробелам на список строк.
    #      '1 2 2 3' -> ['1', '2', '2', '3']
    string_list = user_input_string.split()

    # 2.2. Используем цикл, чтобы превратить каждую строку из списка в целое число (int).
    #      ['1', '2', '2', '3'] -> [1, 2, 2, 3]
    numbers_list = []
    for item in string_list:
        numbers_list.append(int(item))

    # 3. Вызываем нашу основную функцию, чтобы получить ответ (True или False).
    is_mono = is_monotonic(numbers_list)

    # 4. Выводим финальный ответ в зависимости от результата.
    if is_mono:
        # Эта строка выполнится, если is_monotonic вернула True
        print(f"Ваш массив {numbers_list} является монотонным.")
    else:
        # Эта строка выполнится, если is_monotonic вернула False
        print(f"Ваш массив {numbers_list} не является монотонным.")

except ValueError:
    # Этот блок 'except' сработает, если пользователь введет не числа
    # (например, "a b c"), и функция int() выдаст ошибку ValueError.
    # Это простая защита от "падения" программы.
    print("Ошибка: пожалуйста, вводите только числа, разделенные пробелами.")

