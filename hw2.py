# Задание: Реализация структуры данных «Участники спора»
# Создаем пустой список для хранения участников
participants = []

# Запрашиваем данные для трёх участников
for i in range(3):
    print(f"\nВведите данные для участника {i+1}:")
    
    # Запрос параметров участника
    name = input("Наименование: ")
    status = input("Статус: ")
    inn = input("ИНН: ")
    
    # Создаем словарь с данными участника
    participant = {
        "name": name,
        "status": status,
        "inn": inn
    }
    
    # Добавляем участника в список
    participants.append(participant)

# Выводим итоговую структуру
print("\nСписок участников спора:")
print(participants)
